namespace Filterartifact
{
    //----------------------------------------------------------------------------
    public class GameState : State
    {
        //----------------------------------------------------------------------------
        public GameState(int stateid, State parent) : base(stateid, parent)
        {

        }
        //----------------------------------------------------------------------------
        //public function
        //----------------------------------------------------------------------------
        public void SetNextSubState(int stateID)
        {
            NextSubStateID = stateID;
        }
        //----------------------------------------------------------------------------
        public bool GotoSubState(int stateID)
        {
            return SetSubState(stateID, null);
        }
        //----------------------------------------------------------------------------
        public bool GotoSubState(int stateID,object[]parameter)
        {
            return SetSubState(stateID, parameter);
        }
        //----------------------------------------------------------------------------
        public bool GotoBrotherState(int stateID)
        {
            return GetParent().GotoSubState(stateID);
        }
        //----------------------------------------------------------------------------
        public bool GotoBrotherState(int stateID,object[]parameter)
        {
            return GetParent().GotoSubState(stateID, parameter);
        }
        //----------------------------------------------------------------------------
        protected GameState GetParent()
        {
            return (GameState)GetParentState();
        }
        //----------------------------------------------------------------------------
        //----------------------------------------------------------------------------
        //----------------------------------------------------------------------------
        //----------------------------------------------------------------------------
        //----------------------------------------------------------------------------
        //----------------------------------------------------------------------------
        //----------------------------------------------------------------------------
    }

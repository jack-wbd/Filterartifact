namespace Filterartifact
{
    //----------------------------------------------------------------------------
    public class AssetLayer : BaseLayer
    {
        //veriable
        //------------------------------------------------------------------------
        private AssetsManager m_managerAsset = null;
        //constructor
        //------------------------------------------------------------------------
        public AssetLayer()
        {
            m_managerAsset = new AssetsManager();
            m_managerAsset.Initialized();
        }
        //------------------------------------------------------------------------
        public override void Update()
        {
            base.Update();
            m_managerAsset.Update();
        }
        //------------------------------------------------------------------------
        public override void Finalized()
        {
            base.Finalized();
            m_managerAsset.Destroy();
            m_managerAsset = null;
        }
        //------------------------------------------------------------------------
        public AssetsManager GetManager()
        {
            return m_managerAsset;
        }
        //------------------------------------------------------------------------
    }
    //----------------------------------------------------------------------------
}
